public class Professeur {
    private String code;
    private String nom;
    private String prenom;
    private String sexe;
    private String statutMatrimonial;
    private String adresse;
    private String lieuNaiss;
    private String dateNaiss;
    private String telephone;
    private String email;
    private String NifCin;
    private String niveau;
    private String coursEnseigne;
    private String filiereAffecte;
    private double salaire;
    private String poste;
    private String anneeAcademique;
    private String Etat;
    private String memo;

    public Professeur() {
    }

    public Professeur(String code, String nom, String prenom, String sexe, String statutMatrimonial, String adresse, String lieuNaiss, String dateNaiss, String telephone, String email, String NifCin, String niveau, String coursEnseigne, String filiereAffecte, double salaire, String poste, String anneeAcademique, String Etat, String memo) {
        this.code = code;
        this.nom = nom;
        this.prenom = prenom;
        this.sexe = sexe;
        this.statutMatrimonial = statutMatrimonial;
        this.adresse = adresse;
        this.lieuNaiss = lieuNaiss;
        this.dateNaiss = dateNaiss;
        this.telephone = telephone;
        this.email = email;
        this.NifCin = NifCin;
        this.niveau = niveau;
        this.coursEnseigne = coursEnseigne;
        this.filiereAffecte = filiereAffecte;
        this.salaire = salaire;
        this.poste = poste;
        this.anneeAcademique = anneeAcademique;
        this.Etat = Etat;
        this.memo = memo;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public String getSexe() {
        return sexe;
    }

    public void setSexe(String sexe) {
        this.sexe = sexe;
    }

    public String getStatutMatrimonial() {
        return statutMatrimonial;
    }

    public void setStatutMatrimonial(String statutMatrimonial) {
        this.statutMatrimonial = statutMatrimonial;
    }

    public String getAdresse() {
        return adresse;
    }

    public void setAdresse(String adresse) {
        this.adresse = adresse;
    }

    public String getLieuNaiss() {
        return lieuNaiss;
    }

    public void setLieuNaiss(String lieuNaiss) {
        this.lieuNaiss = lieuNaiss;
    }

    public String getDateNaiss() {
        return dateNaiss;
    }

    public void setDateNaiss(String dateNaiss) {
        this.dateNaiss = dateNaiss;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNifCin() {
        return NifCin;
    }

    public void setNifCin(String NifCin) {
        this.NifCin = NifCin;
    }

    public String getNiveau() {
        return niveau;
    }

    public void setNiveau(String niveau) {
        this.niveau = niveau;
    }

    public String getCoursEnseigne() {
        return coursEnseigne;
    }

    public void setCoursEnseigne(String coursEnseigne) {
        this.coursEnseigne = coursEnseigne;
    }

    public String getFiliereAffecte() {
        return filiereAffecte;
    }

    public void setFiliereAffecte(String filiereAffecte) {
        this.filiereAffecte = filiereAffecte;
    }

    public double getSalaire() {
        return salaire;
    }

    public void setSalaire(double salaire) {
        this.salaire = salaire;
    }

    public String getPoste() {
        return poste;
    }

    public void setPoste(String poste) {
        this.poste = poste;
    }

    public String getAnneeAcademique() {
        return anneeAcademique;
    }

    public void setAnneeAcademique(String anneeAcademique) {
        this.anneeAcademique = anneeAcademique;
    }

    public String getEtat() {
        return Etat;
    }

    public void setEtat(String Etat) {
        this.Etat = Etat;
    }

    public String getMemo() {
        return memo;
    }

    public void setMemo(String memo) {
        this.memo = memo;
    }
    
    
    
}

